		<h2>BACKUP AND MAINTANCE</h2>
			<p>
			Media Center stores all information about the files you've Imported, the structure and design of your Views, playlists, and other information in its own internal database called a Library.<br>
<br>
Because this information is critical to all use of MC, it includes a backup and restore system for the Library (and other Settings). When you create a Library Backup, it saves a snapshot of your current Library and all other user settings to a ZIP file which can be restored onto other copies of Media Center (to duplicate the setup) or in case of trouble.<br>
<br>
The Library portion of a backup contains:<br>
<br>
<b>.</b> all imported files and their metadata<br>
<b>.</b> all Playlists and Smartlists<br>
<b>.</b> all Media Views, including those for Theater View and Media Network.<br>
<b>.</b> custom Fields defined in the Library Fields Manager.<br>
The Settings portion of a backup contains:<br>
<br>
<b>.</b> all settings and options under Tools > Options, aside from the items referenced above.<br>
<b>.</b> all DSP Settings<br>
<b>.</b> Zones<br>
<b>.</b> the locations of all Libraries added to the Library Manager<br>
				<br>
The zip files created by the Library Backup system contain both the Library and Settings portions. When you restore a Library Backup, you can choose to restore just the Library, just the Settings, or both.
			</p>
